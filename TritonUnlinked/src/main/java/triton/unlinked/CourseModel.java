public class CourseModel {

    private long id;
    private String subject;
    private String number;
    private String desc;

    public long getId() {
        return id;
    }

    public String getSubject() {
        return subject;
    }

    public void setSubject() {
        this.subject = subject;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber() {
        this.number = number;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc() {
        this.desc = desc;
    }

}
